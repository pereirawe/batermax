						<h1>Políticas de Privacidad</h1>
						<h3>1.	GENERALIDADES</h3>

<p>De acuerdo con la definición establecida en la Ley 1581 de 2012, el dato personal es cualquier información vinculada o que pueda asociarse a una o varias personas naturales determinadas o determinables, como el nombre, la edad, el sexo, el estado civil, el domicilio, entre otros.</p>
<p>Estos datos pueden almacenarse en cualquier soporte físico o electrónico y ser tratados de forma manual o automatizada.</p>
<br>
<p>La Ley 1266 de 2008 define los siguientes tipos de datos de carácter personal:</p>

<p>a) Dato privado: “Es el dato que por su naturaleza íntima o reservada sólo es relevante para el Titular”.</p>
<p>b) Dato semiprivado: “Es semiprivado el dato que no tiene naturaleza íntima, reservada, ni pública y cuyo conocimiento o divulgación puede interesar no sólo a su Titular sino a cierto sector o grupo de personas o a la sociedad en general, como el dato financiero y crediticio de actividad comercial o de servicios a que se refiere el Título IV” de la Ley 1266.</p>
<p>c) Dato público: “Es el dato calificado como tal según los mandatos de la Ley o de la Constitución Política y todos aquellos que no sean semiprivados o privados”, de conformidad con la Ley 1266 de 2008. “Son públicos, entre otros, los datos contenidos en documentos públicos, sentencias judiciales debidamente ejecutoriadas que no estén sometidos a reserva y los relativos al estado civil de las personas”.</p>
<p>Adicionalmente, la Ley 1581 de 2012 establece las siguientes categorías especiales de datos personales:</p>
<p>d) Datos sensibles: Son “aquellos que afectan la intimidad del Titular o cuyo uso indebido puede generar su discriminación, tales como aquellos que revelen el origen racial o étnico, la orientación política, las convicciones religiosas o filosóficas, la pertenencia a sindicatos, organizaciones sociales, de derechos humanos o que promueva intereses de cualquier partido político o que garanticen los derechos y garantías de partidos políticos de oposición, así como los datos relativos a la salud, a la vida sexual y los datos biométricos”.</p>
<br>
<p>La Ley 1581 de 2012 prohíbe el tratamiento de datos sensibles con excepción de los siguientes casos:</p>
<p>I.	Cuando el Titular otorga su consentimiento.</p>
<p>II.	El Tratamiento es necesario para salvaguardar el interés vital del Titular y éste se encuentre física o jurídicamente incapacitado.</p>
<p>III.	 El tratamiento es efectuado en el curso de las actividades legítimas y con las debidas garantías por parte de una fundación, ONG,asociación o cualquier otro organismo sin ánimo de lucro, cuya finalidad sea política, filosófica, religiosa o sindical, siempre que se refieran exclusivamente a sus miembros o a las personas que mantengan contactos regulares por razón de su finalidad.</p>
<p>IV.	 El Tratamiento se refiera a datos que sean necesarios para el reconocimiento, ejercicio o defensa de un derecho en un proceso judicial.</p>
<p>V.	el Tratamiento tenga una finalidad histórica, estadística o científica, en este último caso deben adoptarse las medidas conducentes a la supresión de identidad de los Titulares.</p>

<p>e) Datos personales de los niños, niñas y adolescentes: Se debe tener en cuenta que aunque la Ley 1581 de 2012 prohíbe el tratamiento de los datos personales de los niños, niñas y adolescentes, salvo aquellos que por su naturaleza son públicos, la Corte Constitucional precisó que independientemente de la naturaleza del dato, se puede realizar el tratamiento de éstos “siempre y cuando el fin que se persiga con dicho tratamiento responda al interés superior de los niños, niñas y adolescentes y se asegure sin excepción alguna el respeto a sus derechos prevalentes”.</p>

<br>
<p>También la ley define los siguientes roles:</p>
<p>a) Responsable de Tratamiento: “Persona natural o jurídica, pública o privada, que por sí misma o en asocio con otros, decida sobre la base de datos y/o el Tratamiento de los datos”. La Superintendencia de Industria y Comercio, de acuerdo con la ley es Responsable de Tratamiento de datos personales contenidos en sus bases de datos,</p>
<p>b) Encargado del Tratamiento: “Persona natural o jurídica, pública o privada, que por sí misma o en asocio con otros, realice el Tratamiento de datos personales por cuenta del Responsable del Tratamiento”. La SIC podrá realizar el tratamiento de sus datos personales a través de Encargados.</p>
<br>
<p>Adicionalmente, para este documento se incluyen los siguientes roles.</p>
<p>c) Administrador de base de datos personales: Funcionario o Encargado que tiene a cargo y realiza tratamiento a una o más bases de datos que tiene información personal.</p>
<p>d) Gestor: La Oficina Asesora de Planeación llevará el control del registro de las bases de datos con información personal que hay en la SIC y apoyara el ingreso de la información en el Registro Nacional de Base de Datos.</p>
<p>e) Garante: La Oficina de Servicios al Consumidor y de Apoyo Empresarial (OSCAE) coordinará y tramitará la atención y respuesta de las peticiones, quejas y reclamos relacionados con la ley de protección de datos personales que los titulares realicen a la Superintendencia.</p>

<h4>1.1 DISPOSICIONES GENERALES ESTABLECIDAS EN LA LEY 1581 DE 2012 PARA LA PROTECCIÓN DE DATOS PERSONALES</h4>
<p>La Ley 1581 de 2012 desarrolla el derecho constitucional a conocer, actualizar y rectificar la información recogida en bases de datos y los demás derechos, libertades y garantías a que se refieren los artículos 15 y 20 de la Constitución (derecho a la intimidad y derecho a la información, respectivamente).</p>
<p>La citada ley se aplica a los datos personales registrados en cualquier base de datos que los haga susceptibles de tratamiento por parte de entidades públicas o privadas.</p>
<p>Considerando el modo de conservación de una base de datos, se puede distinguir entre bases de datos automatizadas y bases de datos manuales o archivos.</p>
<p>Las bases de datos automatizadas son aquellas que se almacenan y administran con la ayuda de herramientas informáticas.</p>
<p>Las bases de datos manuales o archivos son aquellas cuya información se encuentra organizada y almacenada de manera física, como las fichas de pedidos a proveedores que contengan información personal relativa al proveedor, como nombre, identificación, números de teléfono, correo electrónico, etc.</p>
<p>La ley exceptúa del régimen de protección:</p>
<p>I.	Los archivos y las bases de datos pertenecientes al ámbito personal o doméstico.</p>
<p>II.	Los que tienen por finalidad la seguridad y la defensa nacionales, la prevención, detección, monitoreo y control del lavado de activos y el financiamiento del terrorismo.</p>
<p>III.	Los que tengan como fin y contengan información de inteligencia y contrainteligencia. </p>
<p>IV.	Los de información periodística y otros contenidos editoriales, </p>
<p>V.	Los regulados por la Ley 1266 de 2008 (información financiera y crediticia, comercial, de servicios y proveniente de terceros países).</p>
<p>VI.	 Los regulados por la Ley 79 de 1993 (sobre censos de población y vivienda).</p>
<h4>1.2	DEBERES DEL RESPONSABLE DEL TRATAMIENTO</h4>

<p>El Responsable del Tratamiento ha sido definido por la Ley 1581 de 2012 como la persona natural o jurídica, pública o privada, que por sí misma o en asocio con otros decida sobre la base de datos y/o el tratamiento de los datos.</p>
<p>La Superintendencia de Industria y Comercio, además de ser la autoridad de protección de datos personales tiene la calidad de Responsable del Tratamiento frente a las bases de datos creadas por la entidad.</p>
<p>Son deberes de los Responsables del Tratamiento y, por consiguiente, de la SIC los establecidos en el artículo 17 de la Ley 1581 de 2012:</p>
<p>a) Garantizar al Titular, en todo tiempo, el pleno y efectivo ejercicio del derecho de hábeas data.</p>
<p>b) Solicitar y conservar, en las condiciones previstas en la citada ley, copia de la respectiva autorización otorgada por el Titular.</p>
<p>c) Informar debidamente al Titular sobre la finalidad de la recolección y los derechos que le asisten por virtud de la autorización otorgada.</p>
<p>d) Conservar la información bajo las condiciones de seguridad necesarias para impedir su adulteración, pérdida, consulta, uso o acceso no autorizado o fraudulento.</p>
<p>e) Garantizar que la información que se suministre al Encargado del Tratamiento sea veraz, completa, exacta, actualizada, comprobable y comprensible.</p>
<p>f) Actualizar la información, comunicando de forma oportuna al Encargado del Tratamiento, todas las novedades respecto de los datos que previamente le haya suministrado y adoptar las demás medidas necesarias para que la información suministrada a éste se mantenga actualizada.</p>
<p>g) Rectificar la información cuando sea incorrecta y comunicar lo pertinente al Encargado del Tratamiento.</p>
<p>h) Suministrar al Encargado del Tratamiento, según el caso, únicamente datos cuyo Tratamiento esté previamente autorizado de conformidad con lo previsto en la citada ley.</p>
<p>i) Exigir al Encargado del Tratamiento en todo momento, el respeto a las condiciones de seguridad y privacidad de la información del Titular.</p>
<p>j) Tramitar las consultas y reclamos formulados en los términos señalados en la citada ley.</p>
<p>k) Adoptar un manual interno de políticas y procedimientos para garantizar el adecuado cumplimiento de la citada ley y en especial, para la atención de consultas y reclamos.</p>
<p>l) Informar al Encargado del Tratamiento cuando determinada información se encuentra en discusión por parte del Titular, una vez se haya presentado la reclamación y no haya finalizado el trámite respectivo.</p>
<p>m) Informar a solicitud del Titular sobre el uso dado a sus datos.</p>
<p>n) Informar a la autoridad de protección de datos cuando se presenten violaciones a los códigos de seguridad y existan riesgos en la administración de la información de los Titulares.</p>
<p>o) Cumplir las instrucciones y requerimientos que imparta la Superintendencia de Industria y Comercio”.</p>
<br>
<h4>1.3	DERECHOS DE LOS TITULARES</h4>

<p>La Ley 1581 de 2012 establece que los Titulares de los datos personales tendrán los siguientes derechos:</p>
<p>a) Conocer, actualizar y rectificar sus datos personales frente a los Responsables del Tratamiento o Encargados del Tratamiento. Este derecho se podrá ejercer, entre otros frente a datos parciales,</p>
<p>inexactos, incompletos, fraccionados, que induzcan a error, o aquellos cuyo Tratamiento esté expresamente prohibido o no haya sido autorizado.</p>
<p>b) Solicitar prueba de la autorización otorgada al Responsable del Tratamiento salvo cuando expresamente se exceptúe como requisito para el Tratamiento, de conformidad con lo previsto en el artículo 10 de la citada ley.</p>
<p>c) Ser informado por el Responsable del Tratamiento o el Encargado del Tratamiento, previa solicitud, respecto del uso que le ha dado a sus datos personales.</p>
<p>d) Presentar ante la Superintendencia de Industria y Comercio quejas por infracciones a lo dispuesto en la citada ley y las demás normas que la modifiquen, adicionen o complementen.</p>
<p>e) Revocar la autorización y/o solicitar la supresión del dato cuando en el Tratamiento no se respeten los principios, derechos y garantías constitucionales y legales. La revocatoria y/o supresión procederá cuando la Superintendencia de Industria y Comercio haya determinado que en el Tratamiento el Responsable o Encargado han incurrido en conductas contrarias a la ley y a la Constitución.</p>
<p>f) Acceder en forma gratuita a sus datos personales que hayan sido objeto de Tratamiento.</p>
<p>Adicionalmente, el Decreto reglamentario 1377 de 2013 define que los Responsables deberán conservar prueba de la autorización otorgada por los Titulares de datos personales para el Tratamiento de los mismos.</p>

<h3>2.	POLÍTICAS</h3>

<p>Se establecen las siguientes directrices generales:</p>
<p>Primero: Cumplir con toda la normatividad legal vigente colombiana que dicte disposiciones para la protección de datos personales.</p>
<p>Segundo: Cumplir con la ley de protección de datos personales de acuerdo con lo contemplado en el Código de Ética y Buen Gobierno de la SIC.</p>
<p>Tercero: Los Servidores deben acogerse a las inhabilidades, impedimentos, incompatibilidades y conflicto de intereses contemplados en la Ley 734 de 2002 (Código Disciplinario Único, capitulo cuarto) para el tratamiento de Datos Personales.</p>

<p>a) La SIC realiza el tratamiento de Datos Personales en ejercicio propio de sus funciones legales y para el efecto no requiere la autorización previa, expresa e informada del Titular. Sin embargo, cuando no corresponda a sus funciones deberá obtener la autorización por medio de un documento físico, electrónico, mensaje de datos, Internet, sitio web, o también de manera verbal o telefónica o en cualquier otro formato que permita su posterior consulta a fin de constatar de forma inequívoca que sin el consentimiento del titular los datos nunca hubieran sido capturados y almacenados en medios electrónicos o físicos. Así mismo se podrá obtener por medio de conductas claras e inequívocas del Titular que permitan concluir de una manera razonable que este otorgó su consentimiento para el manejo de sus Datos Personales.</p>
<p>b) La SIC solicitará la autorización a los Titulares de los datos personales y mantendrá las pruebas de ésta, cuando en virtud de las funciones de promoción, divulgación y capacitación, realice invitaciones a charlas, conferencias o eventos que impliquen el Tratamiento de Datos Personales con una finalidad diferente para la cual fueron recolectados inicialmente.</p>
<p>c) En consecuencia, toda labor de tratamiento de Datos Personales realizada en la SIC deberá corresponder al ejercicio de sus funciones legales o a las finalidades mencionadas en la autorización otorgada por el Titular, cuando la situación así lo amerite. De manera particular, las principales finalidades para el tratamiento de Datos Personales que corresponde a la SIC desarrollar en ejercicio de sus funciones legales se relacionan con los siguientes tramites:</p>

<ul>
<li>Propiedad industrial</li>
<li>Protección al consumidor</li>
<li>Control y verificación de reglamentos técnicos y metrología legal</li>
<li>Protección de la competencia</li>
<li>Vigilancia de las cámaras de comercio</li>
<li>Protección de datos personales</li>
<li>Asuntos jurisdiccionales</li>
</ul>
<p>d) El Dato Personal sometido a Tratamiento deberá ser veraz, completo, exacto, actualizado, comprobable y comprensible. La SIC mantendrá la información bajo estas características siempre y cuando el titular informe oportunamente sus novedades.</p>
<p>e) Los Datos Personales solo serán Tratados por aquellos Funcionarios de la SIC que cuenten con el permiso para ello, o quienes dentro de sus funciones tengan a cargo la realización de tales actividades o por los Encargados.</p>
<p>f) La SIC autorizará expresamente al Administrador de las bases de datos para realizar el tratamiento solicitado por el Titular de la información.</p>
<p>g) La SIC no hará disponibles Datos Personales para su acceso a través de Internet u otros medios masivos de comunicación, a menos que se trate de información pública o que se establezcan medidas técnicas que permitan controlar el acceso y restringirlo solo a las personas autorizadas por ley o por el titular.</p>
<p>h) Todo Dato Personal que no sea Dato Público se tratará por la SIC como confidencial, aun cuando la relación contractual o el vínculo entre el Titular del Dato Personal y la SIC haya finalizado. A la terminación de dicho vínculo, tales Datos Personales deben continuar siendo Tratados de acuerdo con lo dispuesto por el Manual de Archivo y Retención Documental GD01-M01.</p>
<p>i) Cada área de la SIC debe evaluar la pertinencia de anonimizar los actos administrativos y/o documentos de carácter público que contengan datos personales, para su publicación.</p>
<p>j) El Titular, directamente o a través de las personas debidamente autorizadas, podrá consultar sus Datos Personales en todo momento y especialmente cada vez que existan modificaciones en las Políticas de Tratamiento de la información.</p>
<p>k) La SIC suministrará, actualizará, ratificará o suprimirá los Datos Personales a solicitud del Titular para corregir información parcial, inexacta, incompleta, fraccionada que induzca al error o aquella que haya sido tratada previa a la vigencia de la ley y que no tenga autorización o sea prohibida.</p>
<p>l) Cuando le sea solicitada información, ya sea mediante una petición, consulta o reclamo por parte del Titular, sobre la manera como son utilizados sus Datos Personales, la SIC deberá entregar dicha información.</p>
<p>m) A solicitud del Titular y cuando no tenga ningún deber legal o contractual de permanecer en las bases de datos de la SIC, los Datos Personales deberán ser eliminados. En caso de proceder una revocatoria de tipo parcial de la autorización para el Tratamiento de Datos Personales para algunas de las finalidades la SIC podrá seguir utilizando los datos para las demás finalidades respecto de las cuales no proceda dicha revocatoria.</p>
<p>n) Las políticas establecidas por la SIC respecto al tratamiento de Datos Personales podrán ser modificadas en cualquier momento. Toda modificación se realizará con apego a la normatividad legal vigente, y las mismas entrarán en vigencia y tendrán efectos desde su publicación a través de los mecanismos dispuestos por la SIC para que los titulares conozcan la política de tratamiento de la información y los cambios que se produzcan en ella.</p>
<p>o) Los Datos Personales solo podrán ser tratados durante el tiempo y en la medida que la finalidad de su tratamiento lo justifique.</p>
<p>p) La SIC será más rigurosa en la aplicación de las políticas de tratamiento de la información cuando se trate del uso de datos personales de los niños, niñas y adolescentes asegurando la protección de sus derechos fundamentales.</p>
<p>q) La SIC podrá intercambiar información de Datos Personales con autoridades gubernamentales o publicas tales como autoridades administrativas, de impuestos, organismos de investigación y autoridades judiciales, cuando la soliciten en ejercicio de sus funciones.</p>
<p>r) Los Datos Personales sujetos a tratamiento deberán ser manejados proveyendo para ello todas las medidas tanto humanas como técnicas para su protección, brindando la seguridad de que ésta no pueda ser copiada, adulterada, eliminada, consultada o de alguna manera utilizada sin autorización o para uso fraudulento.</p>
<p>s) Cuando finalice alguna de la labores de tratamiento de Datos Personales por los Servidores, contratistas o Encargados del tratamiento, y aun después de finalizado su vínculo o relación contractual con la SIC, éstos están obligados a mantener la reserva de la información de acuerdo con la normatividad vigente en la materia.</p>
<p>t) La SIC divulgará en sus servidores, contratistas y terceros encargados del tratamiento las obligaciones que tienen en relación con el tratamiento de Datos Personales mediante campañas y actividades de orden pedagógico.</p>
